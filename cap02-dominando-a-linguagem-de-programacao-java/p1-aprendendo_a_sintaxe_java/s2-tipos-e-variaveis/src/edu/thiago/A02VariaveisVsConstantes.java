package edu.thiago;

public class A02VariaveisVsConstantes {
    public static void main(String[] args) {
        int ano = 2023;
        System.out.println(ano);
        ano = 2024;
        System.out.println(ano);

        final double PI= 3.1415;
        System.out.println(PI);
        /* Constantes são declaradas usando a palavra reservada "final" antes do tipo e por convenção devem
         * ser escritas em CAIXA ALTA. 
         */
    }
}
